import numpy as np
import matplotlib.pyplot as plt

from sklearn.model_selection import train_test_split
from tensorflow.keras.datasets import fashion_mnist
from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import Dense, Flatten
from tensorflow.keras.utils import to_categorical
from tensorflow.keras.layers import Dropout, BatchNormalization
from tensorflow.keras.preprocessing.image import ImageDataGenerator
from tensorflow.keras.callbacks import EarlyStopping
from tensorflow.keras import regularizers

(X_train_full, y_train_full), (X_test, y_test) = fashion_mnist.load_data()

print("Full training set shape:", X_train_full.shape)
print("Test set shape:", X_test.shape)

plt.figure(figsize=(8, 5))
for i in range(10):
    plt.subplot(2, 5, i+1)
    plt.imshow(X_train_full[i], cmap='gray')
    plt.title(y_train_full[i])
    plt.axis('off')
plt.tight_layout()
plt.show()

X_train_full = X_train_full / 255.0
X_test = X_test / 255.0

y_train_full_cat = to_categorical(y_train_full)
y_test_cat = to_categorical(y_test)

X_train, X_val, y_train, y_val = train_test_split(
    X_train_full,
    y_train_full_cat,
    test_size=0.2,
    stratify=y_train_full,
    random_state=42
)

print("Training set:", X_train.shape)
print("Validation set:", X_val.shape)
print("Test set:", X_test.shape)

model = Sequential([
    Flatten(input_shape=(28, 28)),
    Dense(128, activation='relu'),
    Dense(64, activation='relu'),
    Dense(10, activation='softmax')
])

model.compile(
    optimizer='adam',
    loss='categorical_crossentropy',
    metrics=['accuracy']
)

model.summary()

history = model.fit(
    X_train, y_train,
    epochs=20,
    batch_size=32,
    validation_data=(X_val, y_val)
)

val_loss, val_acc = model.evaluate(X_val, y_val)
print("Validation Accuracy:", val_acc)

test_loss, test_acc = model.evaluate(X_test, y_test_cat)
print("Test Accuracy:", test_acc)

plt.plot(history.history['accuracy'], label='Train Accuracy')
plt.plot(history.history['val_accuracy'], label='Validation Accuracy')
plt.title('Accuracy over Epochs')
plt.xlabel('Epochs')
plt.ylabel('Accuracy')
plt.legend()
plt.show()

plt.plot(history.history['loss'], label='Train Loss')
plt.plot(history.history['val_loss'], label='Validation Loss')
plt.title('Loss over Epochs')
plt.xlabel('Epochs')
plt.ylabel('Loss')
plt.legend()
plt.show()

"""**Part B: Mitigating Overfitting**"""

X_train_aug = X_train.reshape(-1, 28, 28, 1)
X_val_aug = X_val.reshape(-1, 28, 28, 1)
X_test_aug = X_test.reshape(-1, 28, 28, 1)


datagen = ImageDataGenerator(
    rotation_range=10,
    zoom_range=0.1,
    horizontal_flip=True
)

datagen.fit(X_train_aug)

model_b = Sequential([
    Flatten(input_shape=(28, 28, 1)),

    Dense(128, activation='relu',
          kernel_regularizer=regularizers.l2(0.001)),
    BatchNormalization(),
    Dropout(0.3),

    Dense(64, activation='relu',
          kernel_regularizer=regularizers.l2(0.001)),
    BatchNormalization(),
    Dropout(0.3),

    Dense(10, activation='softmax')
])

model_b.compile(
    optimizer='adam',
    loss='categorical_crossentropy',
    metrics=['accuracy']
)

model_b.summary()

early_stop = EarlyStopping(
    monitor='val_loss',
    patience=5,
    restore_best_weights=True
)

history_b = model_b.fit(
    datagen.flow(X_train_aug, y_train, batch_size=32),
    epochs=30,
    validation_data=(X_val_aug, y_val),
    callbacks=[early_stop]
)

val_loss_b, val_acc_b = model_b.evaluate(X_val_aug, y_val)
print("Improved Validation Accuracy:", val_acc_b)

test_loss_b, test_acc_b = model_b.evaluate(X_test_aug, y_test_cat)
print("Improved Test Accuracy:", test_acc_b)

plt.plot(history_b.history['accuracy'], label='Train Accuracy')
plt.plot(history_b.history['val_accuracy'], label='Validation Accuracy')
plt.title('Part B Accuracy over Epochs')
plt.xlabel('Epochs')
plt.ylabel('Accuracy')
plt.legend()
plt.show()

plt.plot(history_b.history['loss'], label='Train Loss')
plt.plot(history_b.history['val_loss'], label='Validation Loss')
plt.title('Part B Loss over Epochs')
plt.xlabel('Epochs')
plt.ylabel('Loss')
plt.legend()
plt.show()

"""**Part C: Compare and Reflect**"""

plt.figure(figsize=(10, 5))

plt.plot(history.history['accuracy'], label='Baseline Train Acc')
plt.plot(history.history['val_accuracy'], label='Baseline Val Acc')

plt.plot(history_b.history['accuracy'], label='Improved Train Acc')
plt.plot(history_b.history['val_accuracy'], label='Improved Val Acc')

plt.title('Model Accuracy Comparison')
